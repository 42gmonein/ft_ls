#include "ft_ls.h"

void	ft_putnbr(int n)
{
	char	c;
	if (n == -2147483648 && write(1, "-2147483648", 11))
		return ;
	if (n < 0)
	{
		n = -n;
		write(1, "-", 1);
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		c = n % 10 + '0';
		write(1, &c, 1);
	}
	else
	{
		c = n + '0';;
		write(1, &c, 1);
	}
}
